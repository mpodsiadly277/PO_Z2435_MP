public class Point {
    double x, y, a, b;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
        this.a = a;
        this.b = b;
    }

    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }
    public double getA() {
        return a;
    }
    public double getB() {
        return b;
    }
}
